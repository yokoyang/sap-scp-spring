package com.example.demo;

public class Source2 {
    private String id;
    private double points;

    public Source2() {
    }

    public Source2(String id, double points) {
        this.id = id;
        this.points = points;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public double getPoints() {
        return points;
    }

    public void setPoints(double points) {
        this.points = points;
    }
}
